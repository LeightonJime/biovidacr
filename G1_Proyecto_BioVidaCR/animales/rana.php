        <section class="info-section">
            <h2>Características Generales</h2>
            <ul>
                <li>Pequeña y colorida, reconocida por su toxicidad.</li>
                <li>Color común: cuerpo rojo/anaranjado y extremidades azules o negras; otras variaciones locales.</li>
                <li>Su piel contiene toxinas derivadas de su dieta de hormigas y ácaros.</li>
                <li>Mide entre 17 y 24 mm de longitud corporal.</li>
                <li>Piel lisa y brillante; dedos con discos adhesivos para trepar.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Hábitat y Distribución</h2>
            <ul>
                <li>Presente en tierras bajas húmedas del Caribe costarricense.</li>
                <li>Habita selvas tropicales, bosques secundarios y jardines densamente vegetados.</li>
                <li>Diurna y terrestre, frecuenta hojarasca y vegetación baja.</li>
                <li>A menudo cercana a cuerpos de agua, sin ser acuática.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Dieta y Comportamiento</h2>
            <ul>
                <li>Se alimenta de pequeñas hormigas y ácaros venenosos.</li>
                <li>Captura presas con su lengua pegajosa.</li>
                <li>Machos territoriales con cantos para atraer hembras.</li>
                <li>Machos cuidan los huevos, manteniéndolos húmedos.</li>
                <li>Hembras transportan renacuajos a bromelias; los alimentan con huevos no fertilizados.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Reproducción</h2>
            <ul>
                <li>Pueden reproducirse todo el año con suficiente humedad.</li>
                <li>Los machos cantan para atraer hembras.</li>
                <li>Huevos depositados en lugares protegidos y cuidados por el macho.</li>
                <li>Hembras transportan renacuajos a pequeñas fuentes de agua.</li>
                <li>Metamorfosis se completa en estas pequeñas piscinas.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Estado de Conservación y Amenazas</h2>
            <ul>
                <li>Clasificada como <strong>"Preocupación Menor"</strong> por la UICN globalmente.</li>
                <li>Amenazas en Costa Rica:
                    <ul>
                        <li>Deforestación y fragmentación del hábitat.</li>
                        <li>Colecta ilegal para el comercio de mascotas.</li>
                        <li>Cambio climático y enfermedades.</li>
                    </ul>
                </li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Importancia Ecológica y Cultural</h2>
            <ul>
                <li>Controlan poblaciones de insectos pequeños.</li>
                <li>Atractivo para el ecoturismo, pero requiere observación responsable.</li>
                <li>Ejemplo destacado de diversidad, toxicidad y cuidado parental.</li>
            </ul>
        </section>

