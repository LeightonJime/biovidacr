        <section class="info-section">
            <h2>Características Generales</h2>
            <ul>
                <li>Reconocido por su espectacular pico multicolor.</li>
                <li>Pico grande pero ligero, gracias a su estructura hueca.</li>
                <li>Plumaje negro con pecho amarillo brillante.</li>
                <li>Piel azul visible alrededor de los ojos y base del pico.</li>
                <li>Longitud: 42–55 cm; peso: 380–500 gramos.</li>
                <li>Patas zigodáctilas: agarre firme en ramas.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Hábitat y Distribución en Costa Rica</h2>
            <ul>
                <li>Común en tierras bajas y laderas medias, Caribe y Pacífico.</li>
                <li>Habitan bosques húmedos, secundarios y zonas cultivadas con árboles.</li>
                <li>Rango altitudinal hasta 1900 metros.</li>
                <li>Arborícolas: pasan la mayor parte del tiempo en árboles.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Dieta y Comportamiento</h2>
            <ul>
                <li>Omnívoros: frutas, insectos, reptiles, huevos y polluelos.</li>
                <li>Usan su largo pico para alcanzar alimento y depredar.</li>
                <li>Sociales: vistos en grupos pequeños o parejas.</li>
                <li>Vuelo ondulado, no muy ágil en espacios abiertos.</li>
                <li>Anidan en cavidades de árboles, a menudo abandonadas por carpinteros.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Reproducción</h2>
            <ul>
                <li>Temporada reproductiva en época seca.</li>
                <li>Ponen 2 a 4 huevos blancos en cavidades de árboles.</li>
                <li>Ambos padres incuban durante 15–20 días.</li>
                <li>Polluelos nacen sin plumas y dependen totalmente de los padres.</li>
                <li>Permanecen varias semanas en el nido antes de volar.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Estado de Conservación y Amenazas</h2>
            <ul>
                <li>Clasificado como "Preocupación Menor" por la UICN.</li>
                <li>En Costa Rica, afectados por deforestación y fragmentación del bosque.</li>
                <li>También se enfrentan a la caza ilegal para el tráfico de mascotas.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Importancia Ecológica y Cultural</h2>
            <ul>
                <li>Dispersores de semillas clave para los ecosistemas tropicales.</li>
                <li>Emblema visual del ecoturismo y biodiversidad nacional.</li>
                <li>Muy presentes en el arte y cultura costarricense.</li>
            </ul>
        </section>

