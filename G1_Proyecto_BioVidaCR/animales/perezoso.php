        <section class="info-section">
            <h2>Características Generales</h2>
            <ul>
                <li>Mamíferos arborícolas conocidos por su lentitud.</li>
                <li>Tres garras prominentes en patas delanteras y traseras.</li>
                <li>Pelaje denso de tonos marrones o grises, a veces verdoso por algas simbióticas.</li>
                <li>Cabeza redondeada, hocico corto y ojos pequeños.</li>
                <li>Miden entre 45 y 75 cm, pesan de 2.5 a 5.5 kg.</li>
                <li>Tienen una cola corta y gruesa.</li>
                <li>Pueden girar la cabeza hasta 270 grados.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Hábitat y Distribución en Costa Rica</h2>
            <ul>
                <li>Habitan selvas tropicales, bosques nubosos y secundarios.</li>
                <li>Presentes en ambas costas: Caribe y Pacífico.</li>
                <li>Pasan la mayor parte de su vida en las copas de los árboles.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Dieta y Comportamiento</h2>
            <ul>
                <li>Herbívoros: comen hojas, brotes y frutas.</li>
                <li>Metabolismo lento, lo que les permite vivir con pocas calorías.</li>
                <li>Solitarios, activos de día y noche.</li>
                <li>Se mueven lentamente y con firmeza entre las ramas.</li>
                <li>Defecan una vez por semana en el suelo, momento vulnerable.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Reproducción</h2>
            <ul>
                <li>Pueden reproducirse durante todo el año.</li>
                <li>Una sola cría por parto, tras 6 meses de gestación.</li>
                <li>La cría se aferra a su madre por varios meses.</li>
                <li>Alcanzan madurez sexual entre los 2 y 3 años.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Estado de Conservación y Amenazas</h2>
            <ul>
                <li>Clasificados como "Preocupación Menor" por la UICN.</li>
                <li>Amenazas en Costa Rica: deforestación, urbanización, carreteras.</li>
                <li>Riesgos: atropellos, electrocuciones y mascotismo ilegal.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Importancia Ecológica</h2>
            <ul>
                <li>Ayudan a dispersar semillas de forma pasiva.</li>
                <li>Fuente de alimento para jaguares, ocelotes y aves rapaces.</li>
                <li>Las algas de su pelaje ofrecen camuflaje y microecosistemas.</li>
            </ul>
        </section>

