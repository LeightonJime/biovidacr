        <section class="info-section">
            <h2>Características Generales</h2>
            <ul>
                <li>Guacamaya grande y colorida con plumaje rojo escarlata brillante.</li>
                <li>Ala y cola con plumas amarillas y azul intenso.</li>
                <li>Piel blanca sin plumas alrededor de los ojos, con líneas rojas.</li>
                <li>Pico fuerte, curvado: mandíbula superior color hueso, inferior negra.</li>
                <li>Longitud de 81–96 cm; envergadura de ~1 metro.</li>
                <li>Patas zigodáctilas para trepar y manipular objetos.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Hábitat y Distribución en Costa Rica</h2>
            <ul>
                <li>Dos poblaciones principales:
                    <ul>
                        <li>Península de Osa y Parque Nacional Corcovado.</li>
                        <li>Parque Nacional Carara y alrededores.</li>
                    </ul>
                </li>
                <li>Viven en bosques húmedos, secos y ribereños.</li>
                <li>Arborícolas pero capaces de volar largas distancias por alimento.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Dieta y Comportamiento</h2>
            <ul>
                <li>Frutas, semillas, nueces, flores y néctar.</li>
                <li>Utilizan su pico para romper cáscaras duras.</li>
                <li>Sociales e inteligentes; se observan en parejas o grupos familiares.</li>
                <li>Comunicativas mediante chillidos fuertes.</li>
                <li>Monógamas; parejas de por vida.</li>
                <li>Anidan en cavidades de árboles grandes.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Reproducción</h2>
            <ul>
                <li>Época seca: de 1 a 3 huevos blancos por hembra.</li>
                <li>Incubación: ~25 días, principalmente por la hembra.</li>
                <li>Ambos padres alimentan a los polluelos.</li>
                <li>Los jóvenes permanecen ~90 días en el nido antes de volar.</li>
                <li>Luego siguen aprendiendo de sus padres.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Estado de Conservación y Amenazas</h2>
            <ul>
                <li>Clasificada como <strong>"Preocupación Menor"</strong> por la UICN, pero vulnerable localmente.</li>
                <li>Amenazas:
                    <ul>
                        <li>Deforestación para agricultura y turismo.</li>
                        <li>Tráfico ilegal de aves como mascotas.</li>
                        <li>Falta de sitios para anidar.</li>
                        <li>Electrocución en tendidos eléctricos.</li>
                    </ul>
                </li>
                <li>Existen programas de conservación activos en Costa Rica.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Importancia Ecológica y Cultural</h2>
            <ul>
                <li>Dispersan semillas, contribuyendo a la salud del bosque tropical.</li>
                <li>Icono nacional, vital para el ecoturismo.</li>
                <li>Su presencia fortalece la conciencia ambiental y educación ecológica.</li>
            </ul>
        </section>

