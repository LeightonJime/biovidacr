        <section class="info-section">
            <h2>Características Generales</h2>
            <ul>
                <li>Primate pequeño y ágil, con pelaje corto y denso.</li>
                <li>Espalda gris-oliva, vientre blanco, y máscara facial blanca.</li>
                <li>Pelaje oscuro en la coronilla y orejas pequeñas y redondeadas.</li>
                <li>Cola larga no prensil, usada para el equilibrio.</li>
                <li>Longitud corporal de 25–35 cm, cola de 35–45 cm.</li>
                <li>Peso de entre 0.5 y 1.1 kg.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Hábitat y Distribución en Costa Rica</h2>
            <ul>
                <li>Distribución restringida al Pacífico sur de Costa Rica y parte de Panamá.</li>
                <li>Principalmente en la Península de Osa y el Parque Nacional Manuel Antonio.</li>
                <li>Habitan bosques húmedos tropicales y secundarios costeros.</li>
                <li>Son diurnos y arborícolas, activos en las copas de los árboles.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Dieta y Comportamiento</h2>
            <ul>
                <li>Omnívoros: comen frutas, insectos, néctar, flores, pequeños vertebrados y huevos.</li>
                <li>Viven en grandes grupos de hasta 100 individuos.</li>
                <li>Estructura social compleja, especialmente entre machos en época reproductiva.</li>
                <li>Muy ágiles, con movimientos rápidos entre ramas.</li>
                <li>Usan vocalizaciones variadas para comunicarse.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Reproducción</h2>
            <ul>
                <li>Reproducción sincronizada con la época lluviosa.</li>
                <li>Gestación de 5–6 meses; nace una sola cría por parto.</li>
                <li>Crías son cuidadas por la madre y viajan aferradas a su espalda.</li>
                <li>Madurez sexual entre los 2 y 3 años.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Estado de Conservación y Amenazas</h2>
            <ul>
                <li>Clasificado como <strong>"En Peligro"</strong> por la UICN.</li>
                <li>Principales amenazas: deforestación, desarrollo turístico y expansión urbana.</li>
                <li>También enfrentan riesgos por enfermedades y comercio ilegal de mascotas.</li>
                <li>Conservación activa es clave para su supervivencia.</li>
            </ul>
        </section>

        <section class="info-section">
            <h2>Importancia Ecológica y Cultural</h2>
            <ul>
                <li>Dispersan semillas y polinizan algunas plantas.</li>
                <li>Atractivo para el ecoturismo y economía local.</li>
                <li>Indicador de la salud ecológica de los bosques del Pacífico sur.</li>
            </ul>
        </section>

